void PIDY(int yr, float y, float Tp, float Kp, float Ki, float Kd, float sat, float ksat)
{
	float uysat = 0;

	e1 = yr-round(y);
	up = Kp*e1;
	ui2 = ((2*ui)+(Ki*Tp*(e1 + e0)))/2;
	ud = (Kd*(e1-e0))/Tp;
	uy = up + ui2 + ud;
	if (uy > 90)
	{
		uy = 90;
	}
	if (uy < -90)
	{
		uy = -90;
	}
	ui = ui2;
	e0 = e1;

	return uy;
}
void PIDX(int yrx, float yx, float Tpx, float Kpx, float Kix, float Kdx)
{
	e1x = yrx-round(yx);
	upx = Kpx*e1x;
	ui2x = ((2*uix)+(Kix*Tpx*(e1x + e0x)))/2;
	udx = (Kdx*(e1x-e0x))/Tpx;
	ux = upx + ui2x + udx;
	if (ux > 90)
	{
		ux = 90;
	}
	if (ux < -90)
	{
		ux = -90;
	}
	uix = ui2x;
	e0x = e1x;

	return ux;
}

