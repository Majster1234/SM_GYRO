void adxl_write(uint8_t reg, uint8_t value)
{
	uint8_t data[2];
	data[0] = reg;
	data[1] = value;
	HAL_I2C_Master_Transmit(&hi2c1, adxl_adress, data, 2, 10);
}

void adxl_read (uint8_t reg, uint8_t numberofbytes)
{
HAL_I2C_Mem_Read(&hi2c1, adxl_adress, reg, 1, &data_rec, numberofbytes, 100);
}

void adxl_init (void)
{
	adxl_read (0x00,1);
	adxl_write (0x2d,0);     //resetowanie wszystkich bitów
	adxl_write (0x2d, 0x08); //inicjalizacja pomiarów

	adxl_write (0x31, 0x00); //+-4g zakres
}
void odczyt()
{

	adxl_read (0x32, 6);
	x = (int16_t)((data_rec[1]<<8) | data_rec[0]);
	y = (int16_t)((data_rec[3]<<8) | data_rec[2]);
	z = (int16_t)((data_rec[5]<<8) | data_rec[4]);
	xg = x;//*0.0078;
	yg = y;//*0.0078;
	zg = z;//*0.0078;
	//Rx = x/255.0;
	//Ry = y/255.0;
	//Rz = z/255.0;
	Rx = atan(yg / sqrt(pow(xg, 2) + pow(zg, 2))) * 180 / M_PI;
	Rx = round(Rx*10)/10;
	Ry = atan(-1 * xg / sqrt(pow(yg, 2) + pow(zg, 2))) * 180 / M_PI;
	Ry = round(Ry*10)/10;
	HAL_Delay(10);
}
